function countBs(string) {
//Base Case
  //condition
  if(string.length === 0){
    return 0;
  }
//RECURSION
  //condition
  if(string[0] === 'B'){
    return 1 + countBs(string.slice(1));
  }
  return 0 + countBs(string.slice(1));
}

