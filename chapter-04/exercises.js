// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, increment, result) {
  if(result === undefined){
    result = [];
  }
  
  if(increment === undefined){
    if(start < end){
      increment = 1;
    } else if(start > end){
      increment = -1;
    }
  }
  
  if(start === end && result.length === 0){
    return result;
  }
  
  if(increment < 0 && start < end){
    return result;
  }
  if(increment > 0 && start > end){
    return result;
  }
  
  result.push(start);
  
  if(start === end){
    return result;
  }
  
  if(increment > 0 && start + increment > end){
    return result;
  }
  if(increment < 0 && start + increment < end){
    return result;
  }
  
  return range(start + increment, end, increment, result);

}

